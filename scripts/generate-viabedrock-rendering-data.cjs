'use strict'

const fs = require('fs')
const path = require('path')

const MINECRAFT_VERSION = '1.21.11'
const projectRoot = path.resolve(__dirname, '..')
const outputPath = path.join(projectRoot, 'patches', 'viabedrock-inventory', 'BridgeBlockRenderingData.java')

function wrappedStringConstant (name, values) {
  const chunks = []
  let chunk = ''
  for (const value of values) {
    const next = chunk ? `${chunk} ${value}` : value
    if (next.length > 110) {
      chunks.push(chunk)
      chunk = value
    } else {
      chunk = next
    }
  }
  if (chunk) chunks.push(chunk)

  const lines = chunks.map((value, index) => {
    const suffix = index === chunks.length - 1 ? ';' : ' +'
    const separatedValue = index === 0 ? value : ` ${value}`
    return `            ${JSON.stringify(separatedValue)}${suffix}`
  })
  return `    private static final String ${name} =\n${lines.join('\n')}`
}

function generateSource () {
  const data = require('minecraft-data')(MINECRAFT_VERSION)
  if (!data) throw new Error(`minecraft-data does not support Java ${MINECRAFT_VERSION}`)

  const blocks = [...data.blocksArray].sort((a, b) => a.name.localeCompare(b.name))
  const zeroFilter = blocks.filter(block => block.filterLight === 0).map(block => block.name)
  const partialFilter = blocks
    .filter(block => block.filterLight > 0 && block.filterLight < 15)
    .map(block => `${block.name}=${block.filterLight}`)
  const emission = blocks
    .filter(block => block.emitLight > 0)
    .map(block => `${block.name}=${block.emitLight}`)
  const fullCube = blocks
    .filter(block => data.blockCollisionShapes.blocks[block.name] === 1 && block.filterLight === 15)
    .map(block => block.name)

  // These two 26.1 blocks post-date the minecraft-data Java table used by ViaBedrock.
  zeroFilter.push('golden_dandelion', 'potted_golden_dandelion')
  zeroFilter.sort()

  return `/*
 * Generated by scripts/generate-viabedrock-rendering-data.cjs from
 * minecraft-data Java ${MINECRAFT_VERSION}. Do not edit by hand.
 */
package net.raphimc.viabedrock.protocol.storage;

import java.util.Arrays;
import java.util.HashMap;
import java.util.Map;
import java.util.Set;

final class BridgeBlockRenderingData {

${wrappedStringConstant('ZERO_FILTER_DATA', zeroFilter)}
${wrappedStringConstant('PARTIAL_FILTER_DATA', partialFilter)}
${wrappedStringConstant('EMISSION_DATA', emission)}
${wrappedStringConstant('FULL_CUBE_DATA', fullCube)}

    private static final Set<String> ZERO_FILTER = parseSet(ZERO_FILTER_DATA);
    private static final Map<String, Integer> PARTIAL_FILTER = parseMap(PARTIAL_FILTER_DATA);
    private static final Map<String, Integer> EMISSION = parseMap(EMISSION_DATA);
    private static final Set<String> FULL_CUBE = parseSet(FULL_CUBE_DATA);

    private BridgeBlockRenderingData() {
    }

    static int filterLight(final String identifier) {
        if (ZERO_FILTER.contains(identifier)) return 0;
        return PARTIAL_FILTER.getOrDefault(identifier, 15);
    }

    static int emission(final String identifier) {
        return EMISSION.getOrDefault(identifier, 0);
    }

    static boolean isSolidFullCube(final String identifier) {
        return FULL_CUBE.contains(identifier);
    }

    private static Set<String> parseSet(final String data) {
        if (data.isEmpty()) return Set.of();
        return Set.copyOf(Arrays.asList(data.split(" ")));
    }

    private static Map<String, Integer> parseMap(final String data) {
        final Map<String, Integer> result = new HashMap<>();
        if (data.isEmpty()) return result;

        for (String entry : data.split(" ")) {
            final int separator = entry.lastIndexOf('=');
            result.put(entry.substring(0, separator), Integer.parseInt(entry.substring(separator + 1)));
        }
        return Map.copyOf(result);
    }

}
`
}

const source = generateSource()
if (process.argv.includes('--check')) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : ''
  if (current !== source) {
    throw new Error(`generated ViaBedrock rendering data is stale: ${outputPath}`)
  }
  console.log(`[rendering-data] ${path.relative(projectRoot, outputPath)} is current`)
} else {
  fs.writeFileSync(outputPath, source)
  console.log(`[rendering-data] wrote ${path.relative(projectRoot, outputPath)}`)
}
